var = str(input('Digite qualquer coisa: '))

print('Você digitou:  {:20}' .format(var))
print('Você digitou:  {:>20}' .format(var))
print('Você digitou:  {:^20}' .format(var))
print('Você digitou:  {:=<20}' .format(var))
print('Você digitou:  {:_^20}' .format(var))
print('Você digitou:  {:.>20}' .format(var))
